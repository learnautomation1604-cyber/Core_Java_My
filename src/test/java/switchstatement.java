// Switch statement is used to select one of any code blocks to execute based on the value of an expression.
// It can make core cleaner and easier to read compared to mutiple if-else statements,
// When click the single variables against several possible values


class switchstatement{


    public static void main(String[] args) {

        String day = "Wednessday";


        switch (day) {

            case "Monday":
                System.out.println("Monday");
                break;

            case "Tuesday":
                System.out.println("Tueday");
                break;

            case "Wednesday":
                System.out.println("Wednesday");
                break;

            default:
                System.out.println("Unknown day");
        }


        /// Morning 9 AM - Would be prior





    }








}
